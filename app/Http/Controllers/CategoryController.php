     */
    public function store(Request $request)
    {
            $validatedData = $request->validate([
                'category_name' => 'required|string|max:200',
                'description' => 'required|string|min:10',  
                'type' => 'required|string|max:200',
                ]);
                
              
    
                $create= new Category();
               
                $create->category_name=$request->category_name;
                
                $create->description=$request->description;
                $create->type=$request->type;
                $create->slug=Str::slug($request->category_name,'-');
               $create->parent_category=$request->parent_category;
                $create->save();
               
               
                return response()->json(['message'=>$create->slug]);
                return redirect()->back();
    }

